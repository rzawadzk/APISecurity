class WAFRuleGenerator:
    """Generate WAF rules to block or alert on shadow/unauthorized API endpoints."""

    def __init__(self, db: Database):
        self.db = db

    def generate_nginx_rules(
        self,
        endpoints: list[APIEndpoint] | None = None,
        action: str = "deny",  # "deny" or "return 403"
    ) -> str:
        """Generate Nginx location blocks to block shadow APIs."""
        if endpoints is None:
            endpoints = self.db.get_endpoints_by_status(APIStatus.SHADOW)

        lines = [
            "# ═══════════════════════════════════════════════",
            "# API Scout — Auto-Generated Shadow API Block Rules",
            f"# Generated: {datetime.now().isoformat()}",
            f"# Endpoints: {len(endpoints)}",
            "# ═══════════════════════════════════════════════",
            "",
        ]

        for ep in endpoints:
            # Convert path pattern to Nginx regex
            nginx_path = _path_to_nginx_regex(ep.path_pattern)
            method_check = f'if ($request_method = {ep.method}) ' + '{'

            lines.extend([
                f"# Shadow API: {ep.method} {ep.path_pattern}",
                f"# Calls: {ep.total_calls}, First seen: {ep.first_seen}",
                f"location ~ ^{nginx_path}$ {{",
                f"    {method_check}",
                f"        {action};",
                "    }",
                "}",
                "",
            ])

        return "\n".join(lines)

    def generate_aws_waf_rules(
        self,
        endpoints: list[APIEndpoint] | None = None,
    ) -> dict:
        """Generate AWS WAF rule set JSON to block shadow APIs."""
        if endpoints is None:
            endpoints = self.db.get_endpoints_by_status(APIStatus.SHADOW)

        rules = []
        for i, ep in enumerate(endpoints):
            regex_path = _path_to_waf_regex(ep.path_pattern)
            rules.append({
                "Name": f"BlockShadowAPI_{i}",
                "Priority": 100 + i,
                "Statement": {
                    "AndStatement": {
                        "Statements": [
                            {
                                "ByteMatchStatement": {
                                    "FieldToMatch": {"Method": {}},
                                    "PositionalConstraint": "EXACTLY",
                                    "SearchString": ep.method,
                                    "TextTransformations": [{"Priority": 0, "Type": "NONE"}],
                                }
                            },
                            {
                                "RegexPatternSetReferenceStatement": {
                                    "ARN": "PLACEHOLDER_ARN",
                                    "FieldToMatch": {"UriPath": {}},
                                    "TextTransformations": [{"Priority": 0, "Type": "NONE"}],
                                }
                            },
                        ]
                    }
                },
                "Action": {"Block": {}},
                "VisibilityConfig": {
                    "SampledRequestsEnabled": True,
                    "CloudWatchMetricsEnabled": True,
                    "MetricName": f"BlockShadowAPI_{ep.method}_{_sanitize(ep.path_pattern)}",
                },
            })

        return {
            "_comment": f"Generated by API Scout at {datetime.now().isoformat()}",
            "Name": "APIScout-ShadowAPIBlockRules",
            "Scope": "REGIONAL",
            "DefaultAction": {"Allow": {}},
            "Rules": rules,
            "VisibilityConfig": {
                "SampledRequestsEnabled": True,
                "CloudWatchMetricsEnabled": True,
                "MetricName": "APIScoutShadowAPIRules",
            },
        }

    def generate_modsecurity_rules(
        self,
        endpoints: list[APIEndpoint] | None = None,
    ) -> str:
        """Generate ModSecurity rules to block shadow APIs."""
        if endpoints is None:
            endpoints = self.db.get_endpoints_by_status(APIStatus.SHADOW)

        lines = [
            "# ═══════════════════════════════════════════════",
            "# API Scout — ModSecurity Shadow API Block Rules",
            f"# Generated: {datetime.now().isoformat()}",
            "# ═══════════════════════════════════════════════",
            "",
        ]

        for i, ep in enumerate(endpoints):
            rule_id = 900000 + i
            regex_path = _path_to_waf_regex(ep.path_pattern)
            lines.append(
                f'SecRule REQUEST_URI "@rx ^{regex_path}$" '
                f'"id:{rule_id},'
                f'phase:1,'
                f'deny,'
                f'status:403,'
                f'msg:\'API Scout: Blocked shadow API {ep.method} {ep.path_pattern}\','
                f'chain"'
            )
            lines.append(
                f'    SecRule REQUEST_METHOD "@streq {ep.method}" ""'
            )
            lines.append("")

        return "\n".join(lines)


