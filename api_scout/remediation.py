def _path_to_nginx_regex(path_pattern: str) -> str:
    """Convert /users/{id}/orders to /users/[^/]+/orders."""
    import re
    return re.sub(r'\{[^}]+\}', '[^/]+', path_pattern)


def _path_to_waf_regex(path_pattern: str) -> str:
    """Convert /users/{id}/orders to /users/[^/]+/orders."""
    import re
    return re.sub(r'\{[^}]+\}', '[^/]+', path_pattern)


def _sanitize(s: str) -> str:
    """Sanitize a string for metric names."""
    import re
    return re.sub(r'[^A-Za-z0-9]', '_', s).strip('_')


def _generate_operation_id(method: str, path: str) -> str:
    """Generate a camelCase operationId from method + path."""
    parts = path.strip("/").split("/")
    words = []
    for p in parts:
        if p.startswith("{"):
            words.append("By" + p.strip("{}").title())
        elif not p.startswith(("v1", "v2", "v3", "api")):
            words.append(p.replace("-", "_").title())

    return method.lower() + "".join(words)
